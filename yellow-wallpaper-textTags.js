storySmell = "<p>"; //open the starting html tag

function filterSmell(yellowWallpaper) {
    for (let i = 0; i < yellowWallpaper.length; i++) {
        let tags = yellowWallpaper[i].tags;
        let smellText = "";
        for (let t = 0; t < tags.length; t++) {
            if (tags[t].includes("smell")) {
                smellText = yellowWallpaper[i].text + "<br>";
                storySmell = storySmell + smellText + "<br>";
            }
        }
    }
}
filterSmell(yellowWallpaper); // run the function
storySmell = storySmell + "</p>"; //close the html tag
$("#smell").html(storySmell); //add it to the "smell" div

